class DatingGuide {
  String? dgNo;
  String? title;
  String? contents;
  String? thumb;
  String? category;
  String? userNo;
  String? heart;
  String? regDate;

  String? userName;
  String? userProfile;

  @override
  String toString() {
    return 'DatingGuide{dgNo: $dgNo, title: $title, contents: $contents, thumb: $thumb, category: $category, userNo: $userNo, heart: $heart, regDate: $regDate, userName: $userName, userProfile: $userProfile}';
  }
}
